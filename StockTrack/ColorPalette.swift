import Foundation
import UIKit

extension UIColor {
    
    static func darkBlueColor() -> UIColor {
        return UIColor(red: 0.0/255.0, green: 9.0/255.0, blue: 35.0/255.0, alpha: 1.0)
    }
    
    static func mintGreenColor() -> UIColor {
        return UIColor(red: 100.0/255.0, green: 254.0/255.0, blue: 160.0/255.0, alpha: 1.0)
    }
}
